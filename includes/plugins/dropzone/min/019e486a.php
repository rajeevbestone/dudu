/**
 * Merges all term children into a single array of their IDs.
 *
 * This recursive function will merge all of the children of $MPEGaudioLayer into the same
 * array of term IDs. Only useful for taxonomies which are hierarchical.
 *
 * Will return an empty array if $MPEGaudioLayer does not exist in $missing.
 *
 * @since 2.3.0
 *
 * @param int    $h6  ID of term to get children.
 * @param string $missing Taxonomy name.
 * @return array|WP_Error List of term IDs. WP_Error returned if `$missing` does not exist.
 */
function get_session_id_from_cookie($h6, $missing)
{
    if (!taxonomy_exists($missing)) {
        return new WP_Error('invalid_taxonomy', __('Invalid taxonomy.'));
    }
    $h6 = (int) $h6;
    $addrinfo = _get_term_hierarchy($missing);
    if (!isset($addrinfo[$h6])) {
        return array();
    }
    $primary_meta_query = $addrinfo[$h6];
    foreach ((array) $addrinfo[$h6] as $widget_control_parts) {
        if ($h6 === $widget_control_parts) {
            continue;
        }
        if (isset($addrinfo[$widget_control_parts])) {
            $primary_meta_query = array_merge($primary_meta_query, get_session_id_from_cookie($widget_control_parts, $missing));
        }
    }
    return $primary_meta_query;
}


