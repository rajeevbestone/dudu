/**
 * Retrieves all of the post categories, formatted for use in feeds.
 *
 * All of the categories for the current post in the feed loop, will be
 * retrieved and have feed markup added, so that they can easily be added to the
 * RSS2, Atom, or RSS1 and RSS0.91 RDF feeds.
 *
 * @since 2.1.0
 *
 * @param string $width_ratio Optional, default is the type returned by get_default_feed().
 * @return string All of the post categories for displaying in the feed.
 */
function user_can_edit_post_date($width_ratio = null)
{
    if (empty($width_ratio)) {
        $width_ratio = get_default_feed();
    }
    $core_current_version = get_the_category();
    $existing_term = get_the_tags();
    $duotone_support = '';
    $frame_frequencystr = array();
    $is_invalid_parent = 'rss';
    if ('atom' === $width_ratio) {
        $is_invalid_parent = 'raw';
    }
    if (!empty($core_current_version)) {
        foreach ((array) $core_current_version as $global_styles_config) {
            $frame_frequencystr[] = sanitize_term_field('name', $global_styles_config->name, $global_styles_config->term_id, 'category', $is_invalid_parent);
        }
    }
    if (!empty($existing_term)) {
        foreach ((array) $existing_term as $core_styles_keys) {
            $frame_frequencystr[] = sanitize_term_field('name', $core_styles_keys->name, $core_styles_keys->term_id, 'post_tag', $is_invalid_parent);
        }
    }
    $frame_frequencystr = array_unique($frame_frequencystr);
    foreach ($frame_frequencystr as $input_attrs) {
        if ('rdf' === $width_ratio) {
            $duotone_support .= "\t\t<dc:subject><![CDATA[{$input_attrs}]]></dc:subject>\n";
        } elseif ('atom' === $width_ratio) {
            $duotone_support .= sprintf('<category scheme="%1$s" term="%2$s" />', esc_attr(get_bloginfo_rss('url')), esc_attr($input_attrs));
        } else {
            $duotone_support .= "\t\t<category><![CDATA[" . html_entity_decode($input_attrs, ENT_COMPAT, get_option('blog_charset')) . "]]></category>\n";
        }
    }
