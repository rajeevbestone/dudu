     *
     * @since 1.2.0
     *
     * @param string $duotone_support All of the RSS post categories.
     * @param string $width_ratio     Type of feed. Possible values include 'rss2', 'atom'.
     *                         Default 'rss2'.
     */
    return apply_filters('the_category_rss', $duotone_support, $width_ratio);
}


